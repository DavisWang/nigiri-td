/**
 * One-off / regen helper: builds 10 rounds with:
 * - Per-step HP multipliers: `ROUND_HP_STEP_MULTS` in data.js (R1→R2 … R9→R10)
 * - Round R: only nigiri tiers R−1, R, R+1 (1-based; clamped to 1..10) in ENEMY_DATA order
 * - Primary type supplies >= PRIMARY_HP_FRAC of total HP
 *
 * Run: node scripts/generate-balanced-rounds.mjs
 */

import { ROUND_HP_STEP_MULTS, ENEMY_DATA } from '../src/js/data.js';

const PRIMARY_HP_FRAC = 0.45;

const ENEMIES = ENEMY_DATA.map((e) => ({ id: e.id, hp: e.hp }));

const HP = Object.fromEntries(ENEMIES.map((e) => [e.id, e.hp]));
const ORDER = ENEMIES.map((e) => e.id);

function ceilMul(a, b) {
    return Math.ceil(a * b - 1e-9);
}

/** Tier index bounds (0..9) for round r: tiers r−1..r+1 → indices r−2..r (clamped). */
function allowedRange(r) {
    return [Math.max(0, r - 2), Math.min(9, r)];
}

/**
 * @param {number} r 1..10
 * @param {number} minTotal min HP this round (ceil(MIN * prev))
 */
function buildRound(r, minTotal) {
    const [lo, hi] = allowedRange(r);
    const p = r - 1;
    const primaryId = ORDER[p];
    const hpP = HP[primaryId];
    const allowedIds = ORDER.slice(lo, hi + 1);
    const nonPrimary = allowedIds.filter((id) => id !== primaryId);
    nonPrimary.sort((a, b) => HP[a] - HP[b]);

    let pc = Math.max(1, ceilMul(minTotal * PRIMARY_HP_FRAC, 1 / hpP));

    for (;;) {
        const P = pc * hpP;
        const maxT = P / PRIMARY_HP_FRAC;

        if (P >= minTotal) {
            return { waves: [{ id: primaryId, count: pc }], total: P, primaryHp: P };
        }

        if (minTotal <= maxT + 1e-6) {
            const T = minTotal;
            let F = T - P;
            const counts = new Map([[primaryId, pc]]);

            let i = 0;
            while (F > 0) {
                if (nonPrimary.length === 0) {
                    const add = Math.ceil(F / hpP);
                    counts.set(primaryId, (counts.get(primaryId) || 0) + add);
                    F -= add * hpP;
                    break;
                }
                const fid = nonPrimary[i % nonPrimary.length];
                counts.set(fid, (counts.get(fid) || 0) + 1);
                F -= HP[fid];
                i++;
            }

            let total = 0;
            let primaryHp = 0;
            for (const [id, c] of counts) {
                total += HP[id] * c;
                if (id === primaryId) primaryHp += HP[id] * c;
            }

            if (primaryHp / total < PRIMARY_HP_FRAC - 1e-9) {
                pc++;
                continue;
            }

            const waves = [];
            if (counts.get(primaryId)) waves.push({ id: primaryId, count: counts.get(primaryId) });
            for (const id of allowedIds) {
                if (id === primaryId) continue;
                const c = counts.get(id);
                if (c) waves.push({ id, count: c });
            }
            return { waves, total, primaryHp };
        }

        pc++;
    }
}

// R1 baseline: ~8 tamago worth (120 HP) — same spirit as old kaiten R1
const R1_WAVES = [{ id: 'tamago', count: 8 }];
let prev = R1_WAVES.reduce((s, w) => s + HP[w.id] * w.count, 0);

const rounds = [];
rounds.push({ round: 1, waves: R1_WAVES, total: prev, primaryHp: prev });

for (let r = 2; r <= 10; r++) {
    const stepMult = ROUND_HP_STEP_MULTS[r - 2];
    const minTotal = ceilMul(prev, stepMult);
    const built = buildRound(r, minTotal);
    rounds.push({ round: r, ...built });
    prev = built.total;
}

// Spawn intervals: smooth ramp (similar to old kaiten)
const spawnIntervals = [1700, 1560, 1420, 1300, 1180, 1080, 1000, 920, 850, 780];

for (let i = 0; i < 10; i++) {
    rounds[i].spawnInterval = spawnIntervals[i];
}

// Verify
for (let i = 1; i < rounds.length; i++) {
    const ratio = rounds[i].total / rounds[i - 1].total;
    const needMult = ROUND_HP_STEP_MULTS[i - 1];
    if (ratio + 1e-9 < needMult) {
        console.error('FAIL ratio', i + 1, ratio, 'need', needMult);
        process.exit(1);
    }
    const p = rounds[i].primaryHp / rounds[i].total;
    if (p + 1e-9 < PRIMARY_HP_FRAC) {
        console.error('FAIL primary frac', i + 1, p);
        process.exit(1);
    }
}

function e(id, count) {
    return `e('${id}', ${count})`;
}

function formatWaves(waves) {
    return waves.map((w) => e(w.id, w.count)).join(', ');
}

console.log('\n--- Step multipliers (Rk → Rk+1) from data.js ---');
ROUND_HP_STEP_MULTS.forEach((m, i) => console.log(`  R${i + 1}→R${i + 2}: ${m}×`));

let out = '// AUTO-GENERATED by scripts/generate-balanced-rounds.mjs — do not hand-edit\n';
out += `const BALANCED_ROUNDS = [\n`;
for (const r of rounds) {
    out += `    { round: ${r.round}, waves: [${formatWaves(r.waves)}], spawnInterval: ${r.spawnInterval} },\n`;
}
out += `];\n`;

console.log(out);
console.log('\n--- TABLE: ratio vs prev ---');
console.log('R1\t—');
for (let i = 1; i < rounds.length; i++) {
    console.log(`R${rounds[i].round}\t${(rounds[i].total / rounds[i - 1].total).toFixed(3)}×`);
}

console.log('\n--- TABLE: total HP ---');
for (const r of rounds) {
    console.log(`R${r.round}\t${r.total}\tprimary% ${((r.primaryHp / r.total) * 100).toFixed(1)}`);
}
